exports.getMyTours = catchAsync(async (req, res, next) => {
  //1) Find all bookings

  const bookings = await Booking.find({ user: req.user.id });
  //2) Find tours with the returned IDs

  const tourIDs = bookings.map((el) => el.tour);
  const tours = await Tour.find({ _id: { $in: tourIDs } }); // will select all the tours that are in the toursID array

  res.status(200).render('overview', {
    title: 'My tours',
    tours,
  });
});

exports.updateUserData = catchAsync(async (req, res, next) => {
  //we only have this user on the request if we protect the route also we can only access this route if we are logged in

  const updatedUser = await User.findByIdAndUpdate(
    req.user.id,
    {
      name: req.body.name,
      email: req.body.email,
    },
    {
      new: true,
      runValidators: true,
    }
  );

  res.status(200).render('account', {
    title: 'Your account',
    user: updatedUser,
  });
});
